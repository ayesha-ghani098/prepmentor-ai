package com.prepmentor.backend.enums;

/**
 * Enum representing the status of a question set.
 * <ul>
 * <li>DRAFT - The question set is in draft state.</li>
 * <li>PUBLISHED - The question set has been published.</li>
 * </ul>
 */
public enum QuestionSetStatus {
    /** The question set is in draft state. */
    DRAFT,
    /** The question set has been published. */
    PUBLISHED
}
